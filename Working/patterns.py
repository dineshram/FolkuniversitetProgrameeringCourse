liste = [2, 3, 4, 5]

for i in liste:
    print(i*"*")

def addition():
    sum = 0
    for i in liste:
        sum += i
    print(sum)

def multiply():
    product = 1
    for i in liste:
        product *= i
    print(product)


#calling of the functions
addition()
multiply()

        
        
    
    
               

